package main

import "fmt"

// main function is the entry point of the Go program
func main() {
	// Print a message to the console
	fmt.Println("Hello, World!")
	
	// Printf allows formatted output
	fmt.Printf("Welcome to %s programming!\n", "Go")
}

/* 
Run this program using:
go run 01_hello_world.go

Expected output:
Hello, World!
Welcome to Go programming!
*/
